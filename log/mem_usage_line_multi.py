X=[]
Y=[]
startStamp = []
filePath = []
lineLabel = []

colorTable = {'UKSM':'orange', 'Base':'royalblue', 'PKSM-50':'forestgreen', 'PKSM-100':'red', 'PKSM-200':'darkorchid', 'PKSM-500':'goldenrod', 'KSM-100':'yellow'}



lineNum = int(input('lineNum: '))
idxGap = 1
stopTime = int(input('stop: '))

for i in range(lineNum):
    filePath.append(input('file: '))
    startStamp.append(int(input('start: ')))
    lineLabel.append(input('label: '))
    Y.append([])
    X.append([])

for i in range(lineNum):
    print(i)
    curFile = open(filePath[i], 'r')

    baseFree = 0
    idx = 0.0
    loaderStarted = False
    newFile = True

    while True:
        curLine = curFile.readline().strip('\n')
        if curLine == '':
            if newFile :
                continue
            else:
                break
        newFile = False
        
        curTime = int(curLine)
        curFile.readline()
        curLineArr = [x for x in curFile.readline().strip('\n').split(' ') if x]
        # print(curLineArr)
        curFree = int(curLineArr[2])/1024/1024
        curFile.readline()

        if loaderStarted == False:
            baseFree = curFree


        if curTime >= startStamp[i]:
            loaderStarted = True
            Y[i].append(curFree-baseFree)
            # if 'PKSM-100' in lineLabel[i] and idx >= 121:
            #     Y[i][-1] -= 20
            # if 'UKSM' in lineLabel[i] and idx >= 118:
            #     Y[i][-1] -= 20
            X[i].append(idx)
            idx += 1

        if idx > stopTime:
            break

    curFile.close()

